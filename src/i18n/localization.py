def normalize_locale(value):
    raw = str(value or "").strip().lower()
    if not raw:
        return DEFAULT_LOCALE
    return _QUERY_ALIASES.get(raw, DEFAULT_LOCALE)


def locale_from_search(search_value):
    text = str(search_value or "").strip()
    if text.startswith("?"):
        text = text[1:]
    parsed = parse_qs(text, keep_blank_values=False)
    lang_values = parsed.get("lang") or []
    if not lang_values:
        return None
    return normalize_locale(lang_values[0])


def is_locale_token(value):
    token = str(value or "").strip().lower().replace("_", "-")
    return token in _QUERY_ALIASES


def locale_from_accept_language(header_value):
    raw = str(header_value or "").strip()
    if not raw:
        return None
    for chunk in raw.split(","):
        token = chunk.split(";", 1)[0].strip().lower().replace("_", "-")
        if token in _QUERY_ALIASES:
            return normalize_locale(token)
    return None


def t(locale, key, **kwargs):
    loc = normalize_locale(locale)
    loc_dict = _STRINGS.get(loc, _STRINGS[DEFAULT_LOCALE])
    template = loc_dict.get(key) or _STRINGS[DEFAULT_LOCALE].get(key) or key
    if not kwargs:
        return template
    try:
        return template.format(**kwargs)
    except Exception:
        return template
